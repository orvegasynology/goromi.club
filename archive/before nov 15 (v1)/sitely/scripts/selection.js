        function redirectUser() {
            var select = document.getElementById("select");
            var value = select.value;
            if (value === "daimedane") {
                window.location.href = "damedane.html";
            }
            if (value === "majilove") {
                window.location.href = "https://youtu.be/F9UPGM4oY6w?si=EwCqey48Lrb1QfAg&t=81";
            }

        }